     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->input('comment')){
            $comment = new Comment;
            $comment->board = $request->input('board');
            $comment->comment = $request->input('comment');
            $comment->save();                
        }
        
        return back();
    }

